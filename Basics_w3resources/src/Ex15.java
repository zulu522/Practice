//Write a Java program to swap two variables.

public class Ex15 {
    public static void main(String[] args) {
        int a, b, c;
        a = 5;
        b = 10;
        System.out.println(" a = " + a + " " + "b = " + b);

        c = a;
        a = b;
        b = c;
        System.out.println(" a = " + a + " " + "b = " + b);
    }
}
